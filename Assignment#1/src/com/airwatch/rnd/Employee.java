package com.airwatch.rnd;

public class Employee {
    String name;
    int age;
    float salary;

    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public float getSalary() {
        return salary;
    }

    public void setSalary(float salary) {
        this.salary = salary;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}


